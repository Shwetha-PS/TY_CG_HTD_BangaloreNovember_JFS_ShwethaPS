package com.caps.customtreemap.arraylisttreemap;

public class Student
{
	int age;
	String name;
	public Student(int age, String name) {
		super();
		this.age = age;
		this.name = name;
	}
	@Override
	public String toString() {
		return "age is "+this.age+ " and the name is "+this.name;
	}
	
	
	

}
