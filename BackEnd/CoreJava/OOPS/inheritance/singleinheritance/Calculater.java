package singleinheritance;

public class Calculater 
{
	void sum(int a, int b)
	{
		System.out.println(a+b);
	}

	void subtract(int a, int b)
	{
		System.out.println(a-b);
	}
	
	void multiply(int a, int b)
	{
		System.out.println(a*b);
	}
	
	void division(int a, int b)
	{
		System.out.println(a/b);
	}
}
